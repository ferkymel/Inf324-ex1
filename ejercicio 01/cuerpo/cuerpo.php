      <h2>Biblioteca</h2>
      <p>Las bibliotecas se han utilizado durante siglos como centros de aprendizaje y conocimiento. La Biblioteca de Alejandría, fundada en el siglo III a.C., fue una de las primeras bibliotecas importantes del mundo antiguo. Hoy en día, hay bibliotecas públicas y privadas en todo el mundo que ofrecen una amplia variedad de recursos para la educación y el entretenimiento.</p>
      <p>La organización de una biblioteca puede variar según su tamaño y propósito. Sin embargo, la mayoría de las bibliotecas tienen áreas designadas para diferentes tipos de materiales, como libros infantiles, ficción, no ficción y referencia. Las bibliotecas también pueden tener áreas para estudiar y trabajar en grupo, así como salas de reuniones y espacios para eventos.</p>

