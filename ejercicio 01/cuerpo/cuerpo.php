      <h2>Campus Universitario</h2>
      <img src="image/campus.jpg" alt="Imagen" class="img-fluid">
      <p>El campus universitario es el lugar donde se encuentran los edificios y terrenos de una universidad. En general, el campus incluye las bibliotecas, las facultades y escuelas, las aulas, las residencias para los estudiantes, las zonas deportivas y las áreas de esparcimiento como cafeterías, tiendas, jardines y parques. El término "campus" proviene del latín y significa "llanura". En el siglo XVII, se empezó a utilizar esta palabra para referirse al campo que separaba el College of New Jersey del pueblo de Princeton. Posteriormente este término fue empleado por otras universidades.</p>
      <p>En resumen, el campus universitario es un lugar donde los estudiantes pueden encontrar todo lo que necesitan para su vida académica y social. Es un lugar donde pueden estudiar, hacer deporte, socializar y relajarse.</p>

